    def get_streaming_deep_analysis(self, app_id: str) -> dict[str, Any]:
        """Deep streaming analysis: state growth and watermark progression.

        Returns empty dict if no streaming batch data found.
        """
        fqn = self._fqn("gold_streaming_batch_detail")
        sql = f"""
            SELECT query_id, batch_id,
                   COALESCE(state_memory_used_bytes, 0) AS state_memory_used_bytes,
                   COALESCE(state_num_rows_total, 0) AS state_num_rows_total,
                   COALESCE(state_rows_dropped_by_watermark, 0) AS state_rows_dropped_by_watermark,
                   watermark
            FROM {fqn}
            WHERE app_id = :app_id
            ORDER BY query_id, batch_id
        """
        try:
            with self._get_connection() as conn:
                with conn.cursor() as cursor:
                    cursor.execute(sql, parameters={"app_id": app_id})
                    cols = [desc[0] for desc in cursor.description]
                    rows = [dict(zip(cols, row)) for row in cursor.fetchall()]
        except Exception:
            logger.exception("Failed to get streaming deep analysis for %s", app_id)
            return {}

        if not rows:
            return {}

        # State growth analysis
        stateful_rows = [r for r in rows if float(r.get("state_memory_used_bytes", 0) or 0) > 0]
        state_growth: dict[str, Any] = {}
        if stateful_rows:
            first_mem = float(stateful_rows[0].get("state_memory_used_bytes", 0) or 0)
            last_mem = float(stateful_rows[-1].get("state_memory_used_bytes", 0) or 0)
            n = len(stateful_rows)
            growth_per_batch = (last_mem - first_mem) / max(n - 1, 1) if n > 1 else 0
            total_dropped = sum(
                int(r.get("state_rows_dropped_by_watermark", 0) or 0) for r in stateful_rows
            )
            total_rows = int(stateful_rows[-1].get("state_num_rows_total", 0) or 0)
            # eviction_ratio = dropped / (state + dropped) — proportion of all processed rows that were evicted
            eviction_ratio = total_dropped / max(total_rows + total_dropped, 1)

            state_growth = {
                "first_state_mb": round(first_mem / 1024 / 1024, 2),
                "last_state_mb": round(last_mem / 1024 / 1024, 2),
                "growth_mb_per_batch": round(growth_per_batch / 1024 / 1024, 4),
                "batches_analyzed": n,
                "total_rows_dropped_by_watermark": total_dropped,
                "last_state_rows": total_rows,
                "eviction_ratio": round(eviction_ratio, 4),
            }

        # Watermark progression
        watermarks = [r.get("watermark") for r in rows if r.get("watermark")]
        watermark_info: dict[str, Any] = {}
        if watermarks:
            watermark_info = {
                "first_watermark": str(watermarks[0]),
                "last_watermark": str(watermarks[-1]),
                "total_watermark_updates": len(watermarks),
            }

        if not state_growth and not watermark_info:
            return {}

        result: dict[str, Any] = {}
        if state_growth:
            result["state_growth"] = state_growth
        if watermark_info:
            result["watermark"] = watermark_info
        return result

