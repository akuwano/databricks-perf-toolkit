    def write_narrative_summary(
        self,
        app_id: str,
        model_name: str,
        output_lang: str,
        summary_text: str,
        job_analysis_text: str,
        node_analysis_text: str,
        top3_text: str,
        prompt_tokens: int,
        total_tokens: int,
        experiment_id: str = "",
        variant: str = "",
    ) -> bool:
        """Write LLM narrative to gold_narrative_summary via SQL MERGE.

        Returns True on success. Cost is stored in gold_application_summary
        (ETL-computed) and no longer duplicated here (deprecated in v5.12).
        """
        fqn = self._fqn("gold_narrative_summary")
        # Ensure table exists
        create_ddl = f"""
            CREATE TABLE IF NOT EXISTS {fqn} (
                generated_at       TIMESTAMP,
                app_id             STRING,
                model_name         STRING,
                schema_name        STRING,
                output_lang        STRING,
                summary_text       STRING,
                job_analysis_text  STRING,
                node_analysis_text STRING,
                top3_text          STRING,
                prompt_tokens      INT,
                total_tokens       INT,
                experiment_id      STRING,
                variant            STRING
            )
            USING DELTA
        """
        merge_sql = f"""
            MERGE INTO {fqn} AS t
            USING (SELECT
                CURRENT_TIMESTAMP() AS generated_at,
                :app_id AS app_id,
                :model_name AS model_name,
                :schema_name AS schema_name,
                :output_lang AS output_lang,
                :summary_text AS summary_text,
                :job_analysis_text AS job_analysis_text,
                :node_analysis_text AS node_analysis_text,
                :top3_text AS top3_text,
                :prompt_tokens AS prompt_tokens,
                :total_tokens AS total_tokens,
                :experiment_id AS experiment_id,
                :variant AS variant
            ) AS s
            ON t.app_id = s.app_id
            WHEN MATCHED THEN UPDATE SET
                t.generated_at       = s.generated_at,
                t.model_name         = s.model_name,
                t.schema_name        = s.schema_name,
                t.output_lang        = s.output_lang,
                t.summary_text       = s.summary_text,
                t.job_analysis_text  = s.job_analysis_text,
                t.node_analysis_text = s.node_analysis_text,
                t.top3_text          = s.top3_text,
                t.prompt_tokens      = s.prompt_tokens,
                t.total_tokens       = s.total_tokens,
                t.experiment_id      = s.experiment_id,
                t.variant            = s.variant
            WHEN NOT MATCHED THEN INSERT *
        """
        params = {
            "app_id": app_id,
            "model_name": model_name,
            "schema_name": f"{self._config.catalog}.{self._config.schema}",
            "output_lang": output_lang,
            "summary_text": summary_text,
            "job_analysis_text": job_analysis_text,
            "node_analysis_text": node_analysis_text,
            "top3_text": top3_text,
            "prompt_tokens": prompt_tokens,
            "total_tokens": total_tokens,
            "experiment_id": experiment_id or None,
            "variant": variant or None,
        }
        try:
            with self._get_connection() as conn:
                with conn.cursor() as cursor:
                    cursor.execute(create_ddl)
                    # Best-effort migration: drop deprecated estimated_cost_usd column.
                    # Requires Delta column mapping — skipped silently if unsupported.
                    try:
                        cursor.execute(f"DESCRIBE TABLE {fqn}")
                        existing = {row[0].lower() for row in cursor.fetchall()}
                        if "estimated_cost_usd" in existing:
                            logger.info(
                                "Dropping deprecated column estimated_cost_usd from %s", fqn
                            )
                            cursor.execute(
                                f"ALTER TABLE {fqn} "
                                f"SET TBLPROPERTIES ("
                                f"'delta.columnMapping.mode' = 'name', "
                                f"'delta.minReaderVersion' = '2', "
                                f"'delta.minWriterVersion' = '5')"
                            )
                            cursor.execute(f"ALTER TABLE {fqn} DROP COLUMN estimated_cost_usd")
                    except Exception:
                        logger.debug(
                            "Deprecated column drop skipped for %s (safe to ignore)",
                            fqn,
                            exc_info=True,
                        )
                    cursor.execute(merge_sql, parameters=params)
            logger.info("Narrative written for app_id=%s", app_id)
            return True
        except Exception:
            logger.exception("Failed to write narrative summary for app_id=%s", app_id)
            return False

