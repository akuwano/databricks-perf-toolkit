    def get_skew_analysis(self, app_id: str) -> dict[str, Any]:
        """Analyze data skew: skewed stages + join SQL correlation.

        Returns empty dict if no DATA_SKEW stages found.
        """
        # 1. Skew stages (Top 5 by skew ratio)
        stage_fqn = self._fqn("gold_stage_performance")
        stage_sql = f"""
            SELECT stage_id, stage_name, duration_ms,
                   task_skew_ratio, shuffle_skew_ratio,
                   data_skew_gap_mb, data_skew_gap_p50_mb,
                   task_p50_ms, task_max_ms, task_count,
                   shuffle_read_mb, shuffle_write_mb
            FROM {stage_fqn}
            WHERE app_id = :app_id AND bottleneck_type = 'DATA_SKEW'
            ORDER BY task_skew_ratio DESC
            LIMIT 5
        """
        try:
            with self._get_connection() as conn:
                with conn.cursor() as cursor:
                    cursor.execute(stage_sql, parameters={"app_id": app_id})
                    cols = [desc[0] for desc in cursor.description]
                    skew_stages = [dict(zip(cols, row)) for row in cursor.fetchall()]
        except Exception:
            logger.exception("Failed to get skew stages for %s", app_id)
            return {}

        if not skew_stages:
            return {}

        # 2. Join SQLs (Top 10 with joins, SMJ first)
        sql_fqn = self._fqn("gold_sql_photon_analysis")
        join_sql = f"""
            SELECT execution_id,
                   SUBSTRING(description_short, 1, 80) AS description_short,
                   ROUND(duration_sec, 1) AS duration_sec,
                   smj_count, bhj_count, photon_bhj_count, total_join_count
            FROM {sql_fqn}
            WHERE app_id = :app_id AND total_join_count > 0
            ORDER BY smj_count DESC, duration_sec DESC
            LIMIT 10
        """
        try:
            with self._get_connection() as conn:
                with conn.cursor() as cursor:
                    cursor.execute(join_sql, parameters={"app_id": app_id})
                    cols = [desc[0] for desc in cursor.description]
                    join_sqls = [dict(zip(cols, row)) for row in cursor.fetchall()]
        except Exception:
            logger.exception("Failed to get join SQLs for %s", app_id)
            join_sqls = []

        # Summary
        max_ratio = max(float(s.get("task_skew_ratio", 0) or 0) for s in skew_stages)
        has_shuffle_skew = any(float(s.get("shuffle_skew_ratio", 0) or 0) > 5 for s in skew_stages)
        has_time_skew = any(float(s.get("task_skew_ratio", 0) or 0) > 5 for s in skew_stages)
        has_smj = any(int(s.get("smj_count", 0) or 0) > 0 for s in join_sqls)

        if has_shuffle_skew and has_time_skew:
            dominant = "both"
        elif has_shuffle_skew:
            dominant = "data"
        else:
            dominant = "time"

        return {
            "skew_stages": skew_stages,
            "join_sqls": join_sqls,
            "skew_summary": {
                "total_skew_stages": len(skew_stages),
                "max_skew_ratio": round(max_ratio, 1),
                "dominant_skew_type": dominant,
                "has_smj": has_smj,
            },
        }

