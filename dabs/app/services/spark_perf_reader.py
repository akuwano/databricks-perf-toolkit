    def get_serialization_summary(self, app_id: str) -> dict[str, Any]:
        """Aggregate serialization metrics across all stages.

        Returns empty dict if serialization columns are not yet available
        (ETL re-run required to add deserialize_ms / result_serialize_ms).
        """
        fqn = self._fqn("gold_stage_performance")
        # First check if serialization columns exist (added by ETL v4.29+)
        check_sql = f"SELECT * FROM {fqn} WHERE app_id = :app_id LIMIT 1"
        try:
            with self._get_connection() as conn:
                with conn.cursor() as cursor:
                    cursor.execute(check_sql, parameters={"app_id": app_id})
                    col_names = {desc[0] for desc in cursor.description}
                    if "deserialize_ms" not in col_names:
                        logger.info("Serialization columns not yet in %s — ETL re-run needed", fqn)
                        return {}
        except Exception:
            logger.exception("Failed to check serialization columns in %s", fqn)
            return {}

        sql = f"""
            SELECT ROUND(SUM(COALESCE(deserialize_ms, 0)), 0) AS total_deserialize_ms,
                   ROUND(SUM(COALESCE(result_serialize_ms, 0)), 0) AS total_result_serialize_ms,
                   ROUND(SUM(COALESCE(deserialize_ms, 0)) + SUM(COALESCE(result_serialize_ms, 0)), 0) AS total_serialization_ms,
                   ROUND(SUM(COALESCE(duration_ms, 0)), 0) AS total_exec_run_ms,
                   ROUND(
                       CASE WHEN SUM(COALESCE(duration_ms, 0)) > 0
                            THEN (SUM(COALESCE(deserialize_ms, 0)) + SUM(COALESCE(result_serialize_ms, 0)))
                                 / SUM(COALESCE(duration_ms, 0)) * 100
                            ELSE 0 END, 1
                   ) AS serialization_pct,
                   SUM(CASE WHEN COALESCE(serialization_pct, 0) > 10 THEN 1 ELSE 0 END) AS stages_with_high_ser
            FROM {fqn}
            WHERE app_id = :app_id
        """
        try:
            with self._get_connection() as conn:
                with conn.cursor() as cursor:
                    cursor.execute(sql, parameters={"app_id": app_id})
                    columns = [desc[0] for desc in cursor.description]
                    row = cursor.fetchone()
                    return dict(zip(columns, row)) if row else {}
        except Exception:
            logger.exception("Failed to get serialization summary for %s", app_id)
            return {}

