    def get_streaming_summary(self, app_id: str) -> dict[str, Any]:
        """Composite: aggregate streaming metrics for LLM Fact Pack."""
        queries = self.get_streaming_query_summary(app_id)
        if not queries:
            return {}

        query_count = len(queries)
        total_batches = sum(int(q.get("total_batches", 0) or 0) for q in queries)
        stateful_count = sum(1 for q in queries if q.get("is_stateful") in (True, "true", 1))
        has_exceptions = any(q.get("exception") is not None for q in queries)

        avg_durations = [float(q.get("avg_batch_duration_ms", 0) or 0) for q in queries]
        max_durations = [float(q.get("max_batch_duration_ms", 0) or 0) for q in queries]
        throughputs = [float(q.get("avg_processed_rows_per_sec", 0) or 0) for q in queries]
        state_mems = [float(q.get("max_state_memory_bytes", 0) or 0) for q in queries]

        # Bottleneck counts
        bn_counts: dict[str, int] = {}
        for q in queries:
            bt = q.get("bottleneck_type", "STREAM_OK")
            bn_counts[bt] = bn_counts.get(bt, 0) + 1

        source_types = list({q.get("source_type", "Unknown") for q in queries})
        sink_types = list({q.get("sink_type", "Unknown") for q in queries})

        return {
            "query_count": query_count,
            "total_batches": total_batches,
            "stateful_query_count": stateful_count,
            "has_exceptions": has_exceptions,
            "avg_batch_duration_ms": round(sum(avg_durations) / len(avg_durations), 1)
            if avg_durations
            else 0,
            "max_batch_duration_ms": max(max_durations) if max_durations else 0,
            "avg_throughput_rows_per_sec": round(sum(throughputs) / len(throughputs), 1)
            if throughputs
            else 0,
            "max_state_memory_bytes": max(state_mems) if state_mems else 0,
            "bottleneck_counts": bn_counts,
            "source_types": source_types,
            "sink_types": sink_types,
        }

    def get_streaming_bottleneck_summary(self, app_id: str) -> list[dict[str, Any]]:
        """Aggregate streaming bottleneck counts by type and severity."""
        queries = self.get_streaming_query_summary(app_id)
        summary: dict[str, dict] = {}
        for q in queries:
            bt = q.get("bottleneck_type", "STREAM_OK")
            sev = q.get("severity", "NONE")
            if bt not in summary:
                summary[bt] = {"bottleneck_type": bt, "count": 0, "max_severity": sev}
            summary[bt]["count"] += 1
            sev_rank = {"CRITICAL": 4, "HIGH": 3, "MEDIUM": 2, "LOW": 1, "NONE": 0}
            if sev_rank.get(sev, 0) > sev_rank.get(summary[bt]["max_severity"], 0):
                summary[bt]["max_severity"] = sev
        return list(summary.values())

