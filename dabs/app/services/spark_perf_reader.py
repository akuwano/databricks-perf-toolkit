    def list_compare_results(self, limit: int = 50) -> list[dict[str, Any]]:
        """List past Spark comparison results (without report_markdown)."""
        fqn = self._fqn("spark_compare_result")
        sql = f"""
            SELECT
                comparison_id, compared_at,
                baseline_analyzed_at, baseline_app_id,
                baseline_experiment, baseline_variant,
                baseline_duration_ms, baseline_alerts,
                candidate_analyzed_at, candidate_app_id,
                candidate_experiment, candidate_variant,
                candidate_duration_ms, candidate_alerts,
                regression_detected, regression_severity, net_score
            FROM {fqn}
            ORDER BY compared_at DESC
            LIMIT {limit}
        """
        try:
            with self._get_connection() as conn:
                with conn.cursor() as cursor:
                    cursor.execute(sql)
                    rows = cursor.fetchall()
                    columns = [desc[0] for desc in cursor.description]
                    return [dict(zip(columns, row)) for row in rows]
        except Exception:
            logger.exception("Failed to list spark compare results")
            return []

    def get_compare_result(self, comparison_id: str) -> dict[str, Any] | None:
        """Load a single Spark compare result including report_markdown."""
        fqn = self._fqn("spark_compare_result")
        sql = f"""
            SELECT * FROM {fqn}
            WHERE comparison_id = :comparison_id
            LIMIT 1
        """
        try:
            with self._get_connection() as conn:
                with conn.cursor() as cursor:
                    cursor.execute(sql, parameters={"comparison_id": comparison_id})
                    row = cursor.fetchone()
                    if row is None:
                        return None
                    columns = [desc[0] for desc in cursor.description]
                    return dict(zip(columns, row))
        except Exception:
            logger.exception("Failed to load spark compare result: %s", comparison_id)
            return None
