        # Collect operators from SQL plans
        sql_fqn = self._fqn("gold_sql_photon_analysis")
        collect_sql = f"""
            SELECT execution_id,
                   SUBSTRING(description_short, 1, 80) AS description_short,
                   ROUND(duration_sec, 1) AS duration_sec,
                   non_photon_op_list
            FROM {sql_fqn}
            WHERE app_id = :app_id
              AND (non_photon_op_list LIKE '%CollectLimit%'
                   OR non_photon_op_list LIKE '%LocalLimit%'
                   OR non_photon_op_list LIKE '%TakeOrderedAndProject%')
            ORDER BY duration_sec DESC
            LIMIT 5
        """
        try:
            with self._get_connection() as conn:
                with conn.cursor() as cursor:
                    cursor.execute(collect_sql, parameters={"app_id": app_id})
                    cols = [desc[0] for desc in cursor.description]
                    collect_ops = [dict(zip(cols, row)) for row in cursor.fetchall()]
        except Exception:
            logger.exception("Failed to get collect operators for %s", app_id)
            collect_ops = []

        if not risk_stages and not collect_ops:
            return {}

        # Summarize
        oom_stages = sum(
            1
            for s in risk_stages
            if s.get("failure_reason") and "OutOfMemoryError" in str(s["failure_reason"])
        )
        total_result_mb = sum(float(s.get("total_result_size_mb", 0) or 0) for s in risk_stages)
        large_result_stages = sum(
            1 for s in risk_stages if float(s.get("total_result_size_mb", 0) or 0) > 10
        )

        return {
            "risk_stages": risk_stages,
            "collect_operators": collect_ops,
            "oom_stages": oom_stages,
            "total_result_size_mb": round(total_result_mb, 2),
            "large_result_stages": large_result_stages,
        }

